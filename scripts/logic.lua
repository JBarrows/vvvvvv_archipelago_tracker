function levelOpen(id)
	if id == "0" then -- Overworld
		return true
	elseif id == "1" then -- Space Station 1
		return true
	elseif id == "2" then -- DSS Souleye
		return true
	elseif id == "3" then -- Space Station 2
		return true
	elseif id == "4" then -- Warp Zone
		return true
	elseif id == "5" then -- Tower
		return true
	elseif id == "6" then -- Lab
		return true
	elseif id == "7" then -- Final
		return true
	end
	return true
end
